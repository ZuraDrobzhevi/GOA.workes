#1)კომენტარის სახით ახსენით როდის ვიყენებთ for ციკლს და როდის while ციკლს.


for i in range(10):
    print(i)
#ეს არის for ციკლი და მას ვიყენებთ იმ შემთხვევაში თუ რაიმე კოდის გაშვება გაკეთება გვსურს რამდენჯერმე ოღონდ ვიცით რამდენჯერ.(ბევრჯერ)


i=0
while i < 10:
    print(i + 1)
    i=i+1

#და ეს არის while ციკლი რომელიც გამოიყენება იმ შემთ ხევევაში რედესაც რამისგამეორება გვინდა ბევრჯერ მაგრამ ზუსტად არ ვიცით რამდენჯერ.(შეიძლება ცტა შეიძლება ბევრჯერ.)






